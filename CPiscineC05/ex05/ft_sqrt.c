int	ft_sqrt(int nb)
{
	int	sqrt;

	sqrt = 1;
	if (nb == 2147483647)
		return (0);
	else if (nb <= 0)
		return (0);
	else if (nb == 1)
		return (1);
	while (sqrt < 46341)
	{
		if (sqrt * sqrt == nb)
			return (sqrt);
		sqrt++;
	}
	return (0);
}
